Name = "juAn PaBlo Python "

print(len(Name))              # Int:  Longitud total (10)
print(Name.find("o"))         # Int:  Posición/Índice (4)
print(Name.capitalize())      # Str:  Primera mayúscula
print(Name.upper())           # Str:  Todo mayúsculas
print(Name.lower())           # Str:  Todo minúsculas
print(Name.isdigit())         # Bool: ¿Son números? (False)
print(Name.isalpha())         # Bool: ¿Son letras? (True)
print(Name.count("e"))        # Int:  Cuenta las "e" (2)
print(Name.replace("o", "x")) # Str:  Cambia "o" por "x"
print(Name * 5)    # Str:  Repite el texto 5 veces